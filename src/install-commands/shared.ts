export type ActiveCommandHost = (typeof COMMAND_HOSTS)[number];
export type InstallHost = (typeof INSTALL_HOSTS)[number];
export type CommandHostSelection = InstallHost | "all";

/**
 * Any host a generated Moka adapter file can be attributed to. `codex` and `gemini`
 * are instruction-only hosts: they receive a generated global instruction
 * memory file (upserted via a marker block) but no slash-command/agent adapter,
 * so they need no resourceRoots scan and no HostAdapter.
 */
export type HarnessHost = ActiveCommandHost | "codex" | "gemini";
/**
 * The per-machine host config dirs. Each honors the same env var the host tool
 * itself uses to relocate its config (so containers/CI can redirect, and so
 * does our test suite): Claude Code reads `CLAUDE_CONFIG_DIR`, Codex reads
 * `CODEX_HOME`, OpenCode reads `OPENCODE_CONFIG_DIR`/`XDG_CONFIG_HOME`.
 */
export function claudeGlobalConfigDir(): string {
  return process.env.CLAUDE_CONFIG_DIR ?? join(homedir(), ".claude");
}

export function codexGlobalConfigDir(): string {
  return process.env.CODEX_HOME ?? join(homedir(), ".codex");
}

export function opencodeGlobalConfigDir(): string {
  return (
    process.env.OPENCODE_CONFIG_DIR ??
    join(process.env.XDG_CONFIG_HOME ?? join(homedir(), ".config"), "opencode")
  );
}

function geminiGlobalConfigDir(): string {
  return process.env.GEMINI_CONFIG_DIR ?? join(homedir(), ".gemini");
}

function stripPrefix(value: string, prefix: string): string {
  if (value === prefix) {
    return "";
  }
  return value.startsWith(`${prefix}/`)
    ? value.slice(prefix.length + 1)
    : value;
}

/**
 * Resolve a repo-relative harness path (e.g. ".claude/agents/x.md",
 * ".opencode/opencode.json") to its absolute on-disk target in the
 * per-machine host config dirs. `.claude/*` → the Claude config dir,
 * `.codex/*` → the Codex config dir, `.gemini/*` → the Gemini config dir,
 * and `.opencode/*` plus other root files → the OpenCode config dir. The
 * global `.opencode` and `~/.config/opencode` layouts use identical subdir
 * names, so this is a pure prefix rebase.
 */
export function resolveHarnessTarget(relPath: string): string {
  const normalized = relPath.replaceAll("\\", "/");
  if (normalized === CLAUDE_USER_CONFIG_PATH) {
    return join(dirname(claudeGlobalConfigDir()), CLAUDE_USER_CONFIG_PATH);
  }
  if (normalized === ".claude" || normalized.startsWith(".claude/")) {
    return join(claudeGlobalConfigDir(), stripPrefix(normalized, ".claude"));
  }
  if (normalized === ".codex" || normalized.startsWith(".codex/")) {
    return join(codexGlobalConfigDir(), stripPrefix(normalized, ".codex"));
  }
  if (normalized === ".gemini" || normalized.startsWith(".gemini/")) {
    return join(geminiGlobalConfigDir(), stripPrefix(normalized, ".gemini"));
  }
  return join(opencodeGlobalConfigDir(), stripPrefix(normalized, ".opencode"));
}
