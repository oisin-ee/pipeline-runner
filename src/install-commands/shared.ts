export type EntrypointEntry = [string, PipelineConfig["entrypoints"][string]];
export type ProfileEntry = [string, PipelineConfig["profiles"][string]];

export function profileEntries(config: PipelineConfig): ProfileEntry[] {
  return Object.entries(config.profiles).sort(([a], [b]) => a.localeCompare(b));
}

export function entrypointEntries(config: PipelineConfig): EntrypointEntry[] {
  const entries = Object.entries(config.entrypoints);
  return entries.length > 0
    ? entries
    : [
        [
          "execute",
          {
            description: "Run the configured pipeline workflow",
            workflow: config.default_workflow,
          },
        ],
      ];
}

export function entrypointDescription(
  id: string,
  entrypoint: PipelineConfig["entrypoints"][string]
): string {
  return entrypoint.description ?? `Run the ${id} workflow`;
}

export function instructionsPointer(
  actor: PipelineConfig["profiles"][string]
): string {
  if (actor.instructions.path) {
    return `Instructions: ${actor.instructions.path}`;
  }
  return `Instructions:\n${actor.instructions.inline ?? ""}`;
}

export function compactLines(lines: Array<string | undefined>): string[] {
  return lines.filter((line): line is string => line !== undefined);
}

export function invocationForHost(
  host: ActiveCommandHost,
  entrypointId = "execute"
): string {
  const prefix: Record<ActiveCommandHost, string> = {
    opencode: "/",
    "claude-code": "/",
  };
  return `${prefix[host]}${commandIdForHost(host, entrypointId)} <task description>`;
}

export function commandIdForHost(
  host: ActiveCommandHost,
  entrypointId: string
): string {
  if (host === "opencode" || host === "claude-code") {
    return `${OPENCODE_COMMAND_PREFIX}${entrypointId}`;
  }
  return entrypointId;
}
