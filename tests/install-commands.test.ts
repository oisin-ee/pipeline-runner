import {
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { afterEach, beforeEach, describe, expect, it } from "vitest";
import {
  installCommands as installCommandsImpl,
  parseCommandHost,
} from "../src/install-commands";
import { resolveHarnessTarget } from "../src/install-commands/shared";

// Alias for conciseness — behavior is always global (per-machine host dirs).
const installCommands = installCommandsImpl;

const COMMAND_FRONTMATTER_RE =
  /^---\n[\s\S]*?description: [\s\S]*?\n---\n<!-- Generated by @oisincoveney\/pipeline\. -->/;
const CLAUDE_GATEWAY_AUTH_HEADER = [
  "$",
  "{PIPELINE_MCP_GATEWAY_AUTHORIZATION}",
].join("");

const ENTRYPOINT_COMMAND_SURFACES = [
  ".opencode/commands/moka-quick.md",
  ".opencode/commands/moka-execute.md",
  ".opencode/commands/moka-inspect.md",
];

const CANONICAL_LOCAL_RUN_BY_COMMAND = [
  {
    flags: ["--effort quick"],
    path: ".opencode/commands/moka-quick.md",
  },
  {
    flags: ["--effort thorough"],
    path: ".opencode/commands/moka-execute.md",
  },
  {
    flags: ["--read-only"],
    path: ".opencode/commands/moka-inspect.md",
  },
  {
    flags: ["--effort quick"],
    path: ".claude/commands/moka-quick.md",
  },
  {
    flags: ["--effort thorough"],
    path: ".claude/commands/moka-execute.md",
  },
  {
    flags: ["--read-only"],
    path: ".claude/commands/moka-inspect.md",
  },
] as const;

describe("installCommands", () => {
  let dir: string;
  const savedEnv: Record<string, string | undefined> = {};

  beforeEach(() => {
    dir = mkdtempSync(join(tmpdir(), "pipeline-commands-"));
    // Redirect per-machine host dirs into `dir` so generated files land under
    // it and the existing path assertions (join(dir, ".opencode/…")) work.
    for (const key of [
      "CLAUDE_CONFIG_DIR",
      "CODEX_HOME",
      "OPENCODE_CONFIG_DIR",
      "GEMINI_CONFIG_DIR",
    ]) {
      savedEnv[key] = process.env[key];
    }
    process.env.CLAUDE_CONFIG_DIR = join(dir, ".claude");
    process.env.CODEX_HOME = join(dir, ".codex");
    process.env.OPENCODE_CONFIG_DIR = join(dir, ".opencode");
    process.env.GEMINI_CONFIG_DIR = join(dir, ".gemini");
  });

  afterEach(() => {
    for (const [key, value] of Object.entries(savedEnv)) {
      if (value === undefined) {
        delete process.env[key];
      } else {
        process.env[key] = value;
      }
    }
    rmSync(dir, { recursive: true, force: true });
  });

  it("installs command adapters from package config without repo pipeline files", async () => {
    const result = await installCommands({ cwd: dir, host: "all" });

    expect(existsSync(join(dir, ".pipeline"))).toBe(false);
    expect(result.items.map((item) => item.path)).toContain(
      ".opencode/commands/moka-execute.md"
    );
    expect(result.items.map((item) => item.path)).toContain(
      ".opencode/commands/moka-quick.md"
    );
    expect(existsSync(join(dir, ".agents/skills/execute/SKILL.md"))).toBe(
      false
    );
    expect(existsSync(join(dir, ".opencode/commands/moka-quick.md"))).toBe(
      true
    );
    expect(existsSync(join(dir, ".opencode/commands/pipe.md"))).toBe(false);
    expect(existsSync(join(dir, ".opencode/commands/epic.md"))).toBe(false);
  });

  it("installs all generated host resources from package defaults", async () => {
    const result = await installCommands({ cwd: dir, host: "all" });

    expect(result.items.every((item) => item.action === "create")).toBe(true);
    expect(result.items.map((item) => item.path)).toEqual([
      ".opencode/commands/moka-quick.md",
      ".opencode/commands/moka-execute.md",
      ".opencode/commands/moka-inspect.md",
      ".opencode/opencode.json",
      ".opencode/agents/MoKa Orchestrator.md",
      ".opencode/agents/MoKa Acceptance Reviewer.md",
      ".opencode/agents/MoKa Code Writer.md",
      ".opencode/agents/MoKa Inspector.md",
      ".opencode/agents/MoKa Learner.md",
      ".opencode/agents/MoKa Researcher.md",
      ".opencode/agents/MoKa Schedule Planner.md",
      ".opencode/agents/MoKa Test Writer.md",
      ".opencode/agents/MoKa Thermo Nuclear Reviewer.md",
      ".opencode/agents/MoKa Ticket Scoper.md",
      ".opencode/agents/MoKa Verifier.md",
      ".opencode/plugins/pipeline-goal-context.ts",
      ".claude/commands/moka-quick.md",
      ".claude/commands/moka-execute.md",
      ".claude/commands/moka-inspect.md",
      ".claude/agents/moka-inspector.md",
      ".claude/settings.json",
      ".claude.json",
      ".codex/config.toml",
    ]);
    // AGENTS.md is repo-scoped and must never be written to per-machine host dirs.
    expect(result.items.map((item) => item.path)).not.toContain("AGENTS.md");
    // All items resolve to absolute paths in the redirected host dirs (under dir).
    // Verify by resolving the repo-relative path as the harness resolver would.
    for (const item of result.items) {
      expect(existsSync(resolveHarnessTarget(item.path)), item.path).toBe(true);
    }
    expect(
      readFileSync(join(dir, ".opencode/commands/moka-execute.md"), "utf8")
    ).toMatch(COMMAND_FRONTMATTER_RE);
    const opencode = JSON.parse(
      readFileSync(join(dir, ".opencode/opencode.json"), "utf8")
    );
    expect(opencode.lsp).toBe(true);
    expect(opencode.plugin).toEqual([
      "@devtheops/opencode-plugin-otel@1.1.0",
      "@prevalentware/opencode-goal-plugin",
    ]);
    // Broker credential preparation owns OpenCode provider auth. Generated
    // package defaults only project non-auth plugins and gateway config.
    expect(opencode.provider).toBeUndefined();
    const codexConfig = readFileSync(join(dir, ".codex/config.toml"), "utf8");
    expect(codexConfig).toContain("[features]");
    expect(codexConfig).toContain("hooks = true");
    expect(codexConfig).toContain("[mcp_servers.pipeline-gateway]");
    expect(codexConfig).toContain(
      'url = "https://pipeline-mcp.momokaya.ee/mcp/"'
    );
    expect(codexConfig).toContain(
      "[mcp_servers.pipeline-gateway.env_http_headers]"
    );
    expect(codexConfig).toContain(
      'Authorization = "PIPELINE_MCP_GATEWAY_AUTHORIZATION"'
    );
    expect(existsSync(join(dir, ".opencode/agents/MoKa Orchestrator.md"))).toBe(
      true
    );
    const inspector = readFileSync(
      join(dir, ".opencode/agents/MoKa Inspector.md"),
      "utf8"
    );
    expect(inspector).toContain("mode: all");
    expect(inspector).toContain("hidden: false");
    expect(inspector).toContain("skill:");
    expect(inspector).toContain("research: allow");
    expect(inspector).toContain("task: deny");
    expect(existsSync(join(dir, ".opencode/skills"))).toBe(false);
    const localPlugin = readFileSync(
      join(dir, ".opencode/plugins/pipeline-goal-context.ts"),
      "utf8"
    );
    expect(localPlugin).toContain("Generated by @oisincoveney/pipeline.");
    expect(localPlugin).toContain("@oisincoveney/pipeline:host=opencode");
    expect(localPlugin).toContain("PipelineGoalContext");
    expect(localPlugin).toContain("experimental.session.compacting");
    const quickCommand = readFileSync(
      join(dir, ".opencode/commands/moka-quick.md"),
      "utf8"
    );
    const executeCommand = readFileSync(
      join(dir, ".opencode/commands/moka-execute.md"),
      "utf8"
    );
    expect(quickCommand).toContain("agent: MoKa Orchestrator");
    expect(quickCommand).toContain("Configured orchestrator:");
    expect(quickCommand).toContain("moka run");
    expect(quickCommand).toContain("--effort quick");
    expect(quickCommand).toContain("supervised");
    expect(quickCommand).not.toContain("moka submit");
    expect(executeCommand).toContain("moka run");
    expect(executeCommand).toContain("--effort thorough");
    expect(executeCommand).toContain("supervised");
    expect(executeCommand).not.toContain(
      "Run workflow `inspect` for the user task."
    );
  });

  it("makes Moka specialist agents selectable primary agents and subagents", async () => {
    await installCommands({ cwd: dir, host: "all" });

    for (const agentName of [
      "MoKa Acceptance Reviewer",
      "MoKa Code Writer",
      "MoKa Inspector",
      "MoKa Learner",
      "MoKa Researcher",
      "MoKa Schedule Planner",
      "MoKa Test Writer",
      "MoKa Thermo Nuclear Reviewer",
      "MoKa Verifier",
    ]) {
      const content = readFileSync(
        join(dir, `.opencode/agents/${agentName}.md`),
        "utf8"
      );

      expect(content).toContain("mode: all");
      expect(content).toContain("hidden: false");
    }
  });

  it("installs the pipeline gateway project config for OpenCode", async () => {
    const result = await installCommands({ cwd: dir, host: "all" });

    expect(result.items).toContainEqual(
      expect.objectContaining({
        action: "create",
        host: "opencode",
        path: ".opencode/opencode.json",
      })
    );

    const opencode = JSON.parse(
      readFileSync(join(dir, ".opencode/opencode.json"), "utf8")
    );
    expect(opencode.mcp["pipeline-gateway"]).toMatchObject({
      enabled: true,
      oauth: false,
      type: "remote",
      url: "https://pipeline-mcp.momokaya.ee/mcp/",
    });
    expect(opencode.mcp["pipeline-gateway"].headers).toEqual({
      Authorization: "{env:PIPELINE_MCP_GATEWAY_AUTHORIZATION}",
    });
  });

  it("keeps compatibility command files while pointing at canonical local moka run", async () => {
    await installCommands({ cwd: dir, host: "all" });

    for (const { flags, path } of CANONICAL_LOCAL_RUN_BY_COMMAND) {
      const content = readFileSync(join(dir, path), "utf8");
      expect(content).toContain("moka run");
      expect(content).toContain("local");
      expect(content).toContain("supervised");
      for (const flag of flags) {
        expect(content).toContain(flag);
      }
      expect(content).not.toContain("moka submit");
      expect(content).not.toContain("Argo Workflow");
      expect(content).not.toContain("--kubeconfig <path>");
    }
  });

  it("never emits AGENTS.md — it is repo-scoped and must not appear in per-machine host dirs", async () => {
    const result = await installCommands({ cwd: dir, host: "opencode" });

    expect(result.items.map((item) => item.path)).not.toContain("AGENTS.md");
    // The host dirs should have no AGENTS.md file.
    expect(existsSync(join(dir, ".opencode", "AGENTS.md"))).toBe(false);
    // The cwd itself should not have an AGENTS.md written either.
    expect(existsSync(join(dir, "AGENTS.md"))).toBe(false);
  });

  it("is idempotent and check passes after install for all hosts", async () => {
    await installCommands({ cwd: dir, host: "opencode" });

    const checked = await installCommands({
      check: true,
      cwd: dir,
      host: "opencode",
    });
    expect(checked.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
  });

  it("preserves repo-local OpenCode auth plugin and gateway config", async () => {
    mkdirSync(join(dir, ".opencode"), { recursive: true });
    const existingGateway = {
      enabled: true,
      headers: { Authorization: "{env:CUSTOM_GATEWAY_TOKEN}" },
      oauth: false,
      timeout: 30_000,
      type: "remote",
      url: "https://custom.example/mcp",
    };
    writeFileSync(
      join(dir, ".opencode/opencode.json"),
      `${JSON.stringify(
        {
          $schema: "https://opencode.ai/config.json",
          lsp: false,
          mcp: {
            "pipeline-gateway": existingGateway,
          },
          plugin: ["repo-local-auth-plugin"],
        },
        null,
        2
      )}\n`
    );

    const result = await installCommands({
      cwd: dir,
      force: true,
      host: "all",
    });

    expect(result.items).toContainEqual(
      expect.objectContaining({
        action: "update",
        host: "opencode",
        path: ".opencode/opencode.json",
      })
    );
    const opencode = JSON.parse(
      readFileSync(join(dir, ".opencode/opencode.json"), "utf8")
    );
    expect(opencode.lsp).toBe(false);
    expect(opencode.mcp["pipeline-gateway"]).toEqual(existingGateway);
    expect(opencode.plugin).toEqual([
      "repo-local-auth-plugin",
      "@devtheops/opencode-plugin-otel@1.1.0",
      "@prevalentware/opencode-goal-plugin",
    ]);
  });

  it("preserves existing Codex features while enabling hooks", async () => {
    mkdirSync(join(dir, ".codex"), { recursive: true });
    writeFileSync(
      join(dir, ".codex/config.toml"),
      [
        "[features]",
        "legacy = true",
        "",
        '[projects."/repo"]',
        'trust_level = "trusted"',
        "",
      ].join("\n")
    );

    await installCommands({
      cwd: dir,
      force: true,
      host: "codex",
    });

    const codexConfig = readFileSync(join(dir, ".codex/config.toml"), "utf8");
    expect(codexConfig).toContain("[features]");
    expect(codexConfig).toContain("legacy = true");
    expect(codexConfig).toContain("hooks = true");
    expect(codexConfig).toContain('[projects."/repo"]');
    expect(codexConfig).toContain('trust_level = "trusted"');
  });

  it("is idempotent and check passes after install", async () => {
    await installCommands({ cwd: dir, host: "all" });
    writeFileSync(
      join(dir, ".opencode/commands/execute.md"),
      [
        "---",
        "description: Legacy execute command.",
        "---",
        "<!-- Generated by @oisincoveney/pipeline. -->",
        "<!-- @oisincoveney/pipeline:host=opencode -->",
        "",
      ].join("\n")
    );
    mkdirSync(join(dir, ".opencode/skills/legacy"), { recursive: true });
    writeFileSync(
      join(dir, ".opencode/skills/legacy/SKILL.md"),
      [
        "<!-- Generated by @oisincoveney/pipeline. -->",
        "<!-- @oisincoveney/pipeline:host=opencode -->",
        "",
      ].join("\n")
    );

    const second = await installCommands({ cwd: dir, host: "all" });
    const checked = await installCommands({
      check: true,
      cwd: dir,
      host: "all",
    });

    expect(second.items).toContainEqual(
      expect.objectContaining({
        action: "delete",
        path: ".opencode/commands/execute.md",
      })
    );
    expect(second.items).toContainEqual(
      expect.objectContaining({
        action: "delete",
        path: ".opencode/skills/legacy/SKILL.md",
      })
    );
    expect(checked.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
    expect(checked.items.map((item) => item.path)).not.toContain(
      ".opencode/commands/execute.md"
    );
    expect(checked.items.map((item) => item.path)).not.toContain(
      ".opencode/skills/legacy/SKILL.md"
    );
  });

  it("preserves unowned OpenCode skill files", async () => {
    await installCommands({ cwd: dir, host: "all" });
    mkdirSync(join(dir, ".opencode/skills/execute"), { recursive: true });
    writeFileSync(
      join(dir, ".opencode/skills/execute/SKILL.md"),
      [
        "---",
        "name: execute",
        "description: Installed execute skill.",
        "---",
        "",
        "# Execute",
        "",
      ].join("\n")
    );

    const second = await installCommands({ cwd: dir, host: "all" });

    expect(second.items.map((item) => item.path)).not.toContain(
      ".opencode/skills/execute/SKILL.md"
    );
    expect(
      readFileSync(join(dir, ".opencode/skills/execute/SKILL.md"), "utf8")
    ).toContain("Installed execute skill.");
  });

  it("dry-run reports creates for entrypoint command surfaces without writing files", async () => {
    const result = await installCommands({
      cwd: dir,
      dryRun: true,
      host: "all",
    });

    for (const path of ENTRYPOINT_COMMAND_SURFACES) {
      expect(result.items).toContainEqual(
        expect.objectContaining({ action: "create", path })
      );
      expect(existsSync(join(dir, path))).toBe(false);
    }
    expect(existsSync(join(dir, ".pipeline"))).toBe(false);
  });

  it("check fails when commands are missing", async () => {
    await expect(
      installCommands({ check: true, cwd: dir, host: "opencode" })
    ).rejects.toThrow("not up to date");
  });

  it("dry-run reports conflicts without throwing or overwriting", async () => {
    const path = join(dir, ".opencode/commands/moka-quick.md");
    mkdirSync(join(dir, ".opencode/commands"), { recursive: true });
    writeFileSync(path, "manual command");

    const result = await installCommands({
      cwd: dir,
      dryRun: true,
      host: "opencode",
    });

    expect(result.items[0]).toMatchObject({
      action: "conflict",
      host: "opencode",
      path: ".opencode/commands/moka-quick.md",
    });
    expect(readFileSync(path, "utf8")).toBe("manual command");
  });

  it("refuses to overwrite manually edited files unless forced", async () => {
    const path = join(dir, ".opencode/commands/moka-quick.md");
    mkdirSync(join(dir, ".opencode/commands"), { recursive: true });
    writeFileSync(path, "manual command");

    await expect(
      installCommands({ cwd: dir, host: "opencode" })
    ).rejects.toThrow("Refusing to overwrite");

    const forced = await installCommands({
      cwd: dir,
      force: true,
      host: "opencode",
    });

    expect(forced.items[0]).toMatchObject({
      action: "update",
      host: "opencode",
      path: ".opencode/commands/moka-quick.md",
    });
    expect(readFileSync(path, "utf8")).toContain(
      "Generated by @oisincoveney/pipeline"
    );
  });

  it("validates host names through Commander parser hook", () => {
    expect(parseCommandHost("all")).toBe("all");
    expect(parseCommandHost("opencode")).toBe("opencode");
    expect(parseCommandHost("claude-code")).toBe("claude-code");
    expect(() => parseCommandHost("bad")).toThrow('Unsupported host "bad"');
  });

  it("installs Claude Code command and agent adapters", async () => {
    const result = await installCommands({ cwd: dir, host: "claude-code" });

    expect(result.items.every((item) => item.action === "create")).toBe(true);
    const paths = result.items.map((item) => item.path);
    expect(paths).toContain(".claude/commands/moka-execute.md");
    expect(paths).toContain(".claude/commands/moka-quick.md");
    expect(paths).toContain(".claude/commands/moka-inspect.md");
    expect(paths).toContain(".claude/agents/moka-inspector.md");
    expect(paths).toContain(".claude/settings.json");
    expect(existsSync(join(dir, ".claude/skills"))).toBe(false);

    const execute = readFileSync(
      join(dir, ".claude/commands/moka-execute.md"),
      "utf8"
    );
    expect(execute).toContain("Bash(moka run *)");
    expect(execute).toContain("argument-hint: <task description>");
    expect(execute).toContain("`execute` skill");
    expect(execute).toContain("moka run");
    expect(execute).toContain("--effort thorough");
    expect(execute).toContain("supervised");
    expect(execute).not.toContain("opencode run");
    expect(execute).not.toContain("Task subagent_type");
    expect(execute).not.toContain("Delegate each agent node");

    const inspect = readFileSync(
      join(dir, ".claude/commands/moka-inspect.md"),
      "utf8"
    );
    expect(inspect).toContain("moka run");
    expect(inspect).toContain("--read-only");
    expect(inspect).toContain("supervised");
    expect(inspect).not.toContain("opencode run");
    expect(inspect).not.toContain("Task subagent_type=moka-inspector");

    const agent = readFileSync(
      join(dir, ".claude/agents/moka-inspector.md"),
      "utf8"
    );
    expect(agent).toContain("name: moka-inspector");
    expect(agent).toContain("Generated by @oisincoveney/pipeline");

    const settings = JSON.parse(
      readFileSync(join(dir, ".claude/settings.json"), "utf8")
    );
    expect(settings.permissions.allow).toContain("Bash(moka run *)");
    expect(settings.permissions.allow).not.toContain("Bash(opencode run *)");
    expect(settings.mcpServers).toBeUndefined();

    const claudeUser = JSON.parse(
      readFileSync(join(dir, ".claude.json"), "utf8")
    );
    expect(claudeUser.mcpServers["pipeline-gateway"]).toMatchObject({
      type: "http",
      url: "https://pipeline-mcp.momokaya.ee/mcp/",
    });
    expect(claudeUser.mcpServers["pipeline-gateway"].headers).toEqual({
      Authorization: CLAUDE_GATEWAY_AUTH_HEADER,
    });
  });

  it("is idempotent and check passes for Claude Code", async () => {
    await installCommands({ cwd: dir, host: "claude-code" });

    const second = await installCommands({ cwd: dir, host: "claude-code" });
    expect(second.items.every((item) => item.action === "unchanged")).toBe(
      true
    );

    const checked = await installCommands({
      check: true,
      cwd: dir,
      host: "claude-code",
    });
    expect(checked.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
  });

  it("detects Claude Code drift on generated files via --check", async () => {
    await installCommands({ cwd: dir, host: "claude-code" });

    const path = join(dir, ".claude/commands/moka-execute.md");
    writeFileSync(
      path,
      [
        "<!-- Generated by @oisincoveney/pipeline. -->",
        "<!-- @oisincoveney/pipeline:host=claude-code -->",
        "",
        "drifted body",
        "",
      ].join("\n")
    );

    await expect(
      installCommands({ check: true, cwd: dir, host: "claude-code" })
    ).rejects.toThrow("not up to date");
  });

  it("protects manually edited Claude Code files unless forced", async () => {
    await installCommands({ cwd: dir, host: "claude-code" });

    const path = join(dir, ".claude/commands/moka-execute.md");
    writeFileSync(path, "manually edited");

    await expect(
      installCommands({ cwd: dir, host: "claude-code" })
    ).rejects.toThrow("Refusing to overwrite");

    const forced = await installCommands({
      cwd: dir,
      force: true,
      host: "claude-code",
    });
    expect(
      forced.items.find(
        (item) => item.path === ".claude/commands/moka-execute.md"
      )?.action
    ).toBe("update");
    expect(readFileSync(path, "utf8")).toContain(
      "Generated by @oisincoveney/pipeline"
    );
  });

  it("merges Claude settings and user MCP config without clobbering user-owned keys", async () => {
    const settingsPath = join(dir, ".claude/settings.json");
    mkdirSync(join(dir, ".claude"), { recursive: true });
    writeFileSync(
      settingsPath,
      `${JSON.stringify(
        {
          model: "opus",
          permissions: { allow: ["Read"], deny: ["Bash(rm *)"] },
          mcpServers: { custom: { type: "stdio", command: "x" } },
        },
        null,
        2
      )}\n`
    );
    writeFileSync(
      join(dir, ".claude.json"),
      `${JSON.stringify(
        {
          mcpServers: { custom: { type: "stdio", command: "x" } },
          theme: "dark",
        },
        null,
        2
      )}\n`
    );

    await installCommands({ cwd: dir, force: true, host: "claude-code" });

    const settings = JSON.parse(readFileSync(settingsPath, "utf8"));
    // User-owned keys preserved.
    expect(settings.model).toBe("opus");
    expect(settings.permissions.deny).toEqual(["Bash(rm *)"]);
    expect(settings.mcpServers.custom).toMatchObject({ command: "x" });
    // Generated settings keys injected: allow is unioned.
    expect(settings.permissions.allow).toContain("Read");
    expect(settings.permissions.allow).toContain("Bash(moka run *)");
    expect(settings.permissions.allow).not.toContain("Bash(opencode run *)");

    const claudeUser = JSON.parse(
      readFileSync(join(dir, ".claude.json"), "utf8")
    );
    expect(claudeUser.theme).toBe("dark");
    expect(claudeUser.mcpServers.custom).toMatchObject({ command: "x" });
    expect(claudeUser.mcpServers["pipeline-gateway"]).toMatchObject({
      type: "http",
    });

    // Idempotent: a second install reports no change to the merged settings.
    const second = await installCommands({
      cwd: dir,
      force: true,
      host: "claude-code",
    });
    expect(
      second.items.find((item) => item.path === ".claude/settings.json")?.action
    ).toBe("unchanged");
    expect(
      second.items.find((item) => item.path === ".claude.json")?.action
    ).toBe("unchanged");
  });
});

describe("installCommands (global scope)", () => {
  let dir: string;
  let home: string;
  const savedEnv: Record<string, string | undefined> = {};
  const claudeDir = () => join(home, ".claude");
  const opencodeDir = () => join(home, ".config", "opencode");
  const codexDir = () => join(home, ".codex");
  const geminiDir = () => join(home, ".gemini");

  beforeEach(() => {
    dir = mkdtempSync(join(tmpdir(), "pipeline-commands-repo-"));
    home = mkdtempSync(join(tmpdir(), "pipeline-commands-home-"));
    for (const key of [
      "CLAUDE_CONFIG_DIR",
      "CODEX_HOME",
      "OPENCODE_CONFIG_DIR",
      "GEMINI_CONFIG_DIR",
    ]) {
      savedEnv[key] = process.env[key];
    }
    process.env.CLAUDE_CONFIG_DIR = claudeDir();
    process.env.CODEX_HOME = codexDir();
    process.env.OPENCODE_CONFIG_DIR = opencodeDir();
    process.env.GEMINI_CONFIG_DIR = geminiDir();
  });

  afterEach(() => {
    for (const [key, value] of Object.entries(savedEnv)) {
      if (value === undefined) {
        delete process.env[key];
      } else {
        process.env[key] = value;
      }
    }
    rmSync(dir, { recursive: true, force: true });
    rmSync(home, { recursive: true, force: true });
  });

  it("writes into the redirected per-machine host dirs, not the repo", async () => {
    // Files land in the redirected host dirs; the repo stays clean.
    await installCommandsImpl({ cwd: dir, host: "all" });

    expect(existsSync(join(opencodeDir(), "commands", "moka-execute.md"))).toBe(
      true
    );
    expect(existsSync(join(opencodeDir(), "opencode.json"))).toBe(true);
    expect(existsSync(join(claudeDir(), "commands", "moka-execute.md"))).toBe(
      true
    );
    expect(existsSync(join(claudeDir(), "settings.json"))).toBe(true);
    expect(existsSync(join(dir, ".opencode"))).toBe(false);
    expect(existsSync(join(dir, ".claude"))).toBe(false);
  });

  it("omits the repo-scoped AGENTS.md and stays idempotent across cwds", async () => {
    const first = await installCommandsImpl({
      cwd: dir,
      host: "all",
    });
    expect(first.items.map((item) => item.path)).not.toContain("AGENTS.md");
    expect(existsSync(join(opencodeDir(), "AGENTS.md"))).toBe(false);

    // A second run from a *different* cwd must report no changes — AGENTS.md
    // was the only cwd-dependent surface, so the harness is now stable.
    const otherCwd = mkdtempSync(join(tmpdir(), "pipeline-other-cwd-"));
    try {
      const checked = await installCommandsImpl({
        check: true,
        cwd: otherCwd,
        host: "all",
      });
      expect(checked.items.every((item) => item.action === "unchanged")).toBe(
        true
      );
    } finally {
      rmSync(otherCwd, { recursive: true, force: true });
    }
  });

  it("reports repo-relative item paths for all installed resources", async () => {
    const result = await installCommandsImpl({
      cwd: dir,
      host: "claude-code",
    });

    const paths = result.items.map((item) => item.path);
    expect(paths).toContain(".claude/commands/moka-execute.md");
    expect(paths).toContain(".claude/settings.json");
  });

  it("is idempotent and prunes obsolete files from the host dirs", async () => {
    await installCommandsImpl({ cwd: dir, host: "all" });

    // Plant an obsolete generated command in the global dir.
    writeFileSync(
      join(opencodeDir(), "commands", "execute.md"),
      [
        "---",
        "description: Legacy execute command.",
        "---",
        "<!-- Generated by @oisincoveney/pipeline. -->",
        "<!-- @oisincoveney/pipeline:host=opencode -->",
        "",
      ].join("\n")
    );

    const second = await installCommandsImpl({
      cwd: dir,
      host: "all",
    });
    expect(second.items).toContainEqual(
      expect.objectContaining({
        action: "delete",
        path: ".opencode/commands/execute.md",
      })
    );
    expect(existsSync(join(opencodeDir(), "commands", "execute.md"))).toBe(
      false
    );

    const checked = await installCommandsImpl({
      check: true,
      cwd: dir,
      host: "all",
    });
    expect(checked.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
  });

  it("does not emit instruction files (CLAUDE.md, AGENTS.md, GEMINI.md) — those are owned by rulesync via installRules", async () => {
    // installCommands no longer writes per-host instruction memory files.
    // Global instruction files are generated by installRules (rulesync from oisin-ee/agent/rules).
    const result = await installCommandsImpl({
      cwd: dir,
      host: "all",
    });
    const paths = result.items.map((item) => item.path);
    expect(paths).not.toContain(".claude/CLAUDE.md");
    expect(paths).not.toContain(".codex/AGENTS.md");
    expect(paths).not.toContain(".gemini/GEMINI.md");
    expect(paths).not.toContain("AGENTS.md");
    // The per-machine host dirs never receive instruction files from this path.
    expect(existsSync(join(claudeDir(), "CLAUDE.md"))).toBe(false);
    expect(existsSync(join(codexDir(), "AGENTS.md"))).toBe(false);
    expect(existsSync(join(geminiDir(), "GEMINI.md"))).toBe(false);
  });
});
